package Method;

public class Person {

	
	//field 필드
	 String name;
	 int age;
	 double weight;
	 double height;
	//constructor(초기값)
	public Person(String name, int age, 
		          double weight, double height) {
		this.name = name;
		this.age = age;
		this.weight = weight;
		this.height = height;
	}
	//method
	
	
}


